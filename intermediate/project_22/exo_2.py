str = "M\tE\tR\tC\ti\t"
print(str) # On affiche la chaîne de caractères
print(str.expandtabs()) # On affiche la chaîne de caractères avec des espaces entre chaque caractère
print(str.expandtabs(1)) # On affiche la chaîne de caractères avec un espace entre chaque caractère
print(str.expandtabs(2)) # On affiche la chaîne de caractères avec deux espaces entre chaque caractère
print(str.expandtabs(3)) # On affiche la chaîne de caractères avec trois espaces entre chaque caractère