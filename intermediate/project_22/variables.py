# Aborder les variables en python
# Les variables sont des espaces mémoire dans lesquels on peut stocker des valeurs.
# Ces valeurs peuvent être de différents types : entier, flottant, chaîne de caractères, booléen, liste, dictionnaire, etc.
# Pour définir une variable, on utilise le signe égal (=).
# Par exemple, pour définir une variable nommée age qui contient la valeur 18, on écrit :
# age = 18
# On peut ensuite afficher la valeur de cette variable en utilisant la fonction print(). Par exemple :
# print(age)
# On peut également afficher le type de cette variable en utilisant la fonction type(). Par exemple :
# print(type(age))
# On peut modifier la valeur d'une variable en lui affectant une nouvelle valeur. Par exemple :
# age = 18
# print(age)
# age = 20
# print(age)
# On peut également modifier la valeur d'une variable en utilisant un opérateur. Par exemple :
# age = 18
# print(age)
# age += 2
# print(age)
# age -= 2
# print(age)
# age *= 2
# print(age)
# age /= 2
# print(age)
# age //= 2
# print(age)
# age %= 2
# print(age)
# age **= 2
# print(age)
